// Unpack up to 4 directions from a packed uint32
export function unpackDirections(packed: number): number[] {
  const dirs: number[] = []
  for (let i = 0; i < 4; i++) {
    const dir = (packed >>> (i * 8)) & 0xFF
    if (dir === BORDER_NONE && i > 0) break
    if (i === 0 && dir === BORDER_NONE) break
    dirs.push(dir)
  }
  return dirs
}
